package com.hbm_m.multiblock;

/**
 * Этот интерфейс-маркер должны реализовывать BlockEntity тех мультиблочных контроллеров,
 * которые поддерживают динамическое отображение рамки (например, когда блок ставится сверху).
 * Он предоставляет единый метод для запуска проверки состояния рамки.
 */
public interface IFrameSupportable {

    /**
     * Запускает проверку, должна ли структура в данный момент отображать рамку.
     * Реализация этого метода должна содержать логику сканирования блоков
     * и обновления состояния BlockEntity с последующей синхронизацией с клиентом.
     */
    void checkForFrame();
}