package com.hbm_m.multiblock;

/**
 * Этот интерфейс должны реализовывать BlockEntity тех мультиблочных контроллеров,
 * которые поддерживают динамическое отображение рамки.
 * Он определяет полный контракт для управления состоянием рамки.
 */
public interface IFrameSupportable {

    /**
     * Запускает проверку, должна ли структура в данный момент отображать рамку.
     * Реализация этого метода должна вызывать централизованную логику проверки
     * и затем использовать setFrameVisible() для применения результата.
     */
    void checkForFrame();

    /**
     * Устанавливает новое состояние видимости рамки и синхронизирует его с клиентом.
     * @param visible Новое состояние видимости.
     * @return true, если состояние было изменено, иначе false.
     */
    boolean setFrameVisible(boolean visible);

    /**
     * @return Текущее клиентское состояние видимости рамки. Используется рендерером.
     */
    boolean isFrameVisible();
}
